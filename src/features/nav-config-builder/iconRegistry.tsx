export const iconOptions: IconOption[] = Object.entries(iconRegistry).map(([name, icon]) => ({
  name,
  icon,
  category: getCategoryForIcon(name),
}))

function getCategoryForIcon(name: string): string {
  if (/Home|Menu|Dashboard|Apps|Widget|View|Grid|List/.test(name)) return 'Navigation'
  if (/Add|Edit|Delete|Save|Search|Refresh|Sync|Download|Upload|Print|Share|Send|Copy|Filter|Sort/.test(name)) return 'Actions'
  if (/Person|People|Group|Account|Admin|Supervisor|Badge/.test(name)) return 'Users'
  if (/Folder|File|Attach|Cloud|Storage|Inventory|Archive|Description/.test(name)) return 'Files'
  if (/Email|Mail|Chat|Forum|Message|Comment|Notification|Announcement|Campaign|Phone|Call|Video|Contact/.test(name)) return 'Communication'
  if (/Chart|Trending|Insight|Analytics|Assessment|Leaderboard|Stats|Data/.test(name)) return 'analytics'
  if (/Task|Assignment|Checklist|Playlist|Fact|Pending|Schedule|Time|Calendar|Event|Date|Alarm|Timer|History/.test(name)) return 'Tasks'
  if (/Setting|Tune|Build|Construction|Handy|Engineering|Code|Terminal|Developer|Integration|Api|Extension|Display/.test(name)) return 'Settings'
  if (/Security|Shield|Verified|Gpp|Lock|Key|Vpn|Fingerprint|Privacy|Policy/.test(name)) return 'Security'
  if (/Check|Cancel|Error|Warning|Info|Help|Report|Bug|Release|Priority/.test(name)) return 'Status'
  if (/Flight|Ship|Car|Boat|Train|Rocket|Satellite|Radar|Gps|Explore|Map|Place|Route|Near|Location|Public/.test(name)) return 'Travel'
  if (/Business|Work|Corporate|Balance|Payment|Credit|Receipt|Quote|Price|Money|Savings|Wallet|Shopping|Store/.test(name)) return 'Business'
  if (/Image|Photo|Camera|Video|Movie|Music|Audio|Mic|Volume/.test(name)) return 'Media'
  if (/Form|Input|Text|Check|Radio|Toggle|Format/.test(name)) return 'Forms'
  if (/Star|Favorite|Bookmark|Flag|Label|Tag|Link|Launch|Open|Visibility|Thumb/.test(name)) return 'Misc'
  if (/Arrow|Chevron|Expand|Double|Subdirectory|Swap|Compare|Unfold|Page|Navigate/.test(name)) return 'Arrows'
  if (/Play|Pause|Stop|Skip|Fast|Replay|Shuffle|Repeat|Loop/.test(name)) return 'Player'
  if (/Computer|Desktop|Laptop|Tablet|Phone|Watch|Tv|Monitor|Device|Router|Wifi|Bluetooth|Usb|Printer|Scanner|Keyboard|Mouse|Headphone|Speaker/.test(name)) return 'Devices'
  if (/Sun|Night|Cloud|Thunder|Ac|Water|Air|Thermo|Terrain|Park|Forest|Wave/.test(name)) return 'Weather'
  if (/Palette|Color|Brush|Design|Architecture|Style|Format|Title/.test(name)) return 'Design'
  return 'Other'
}

export const getIconByName = (name: string): ReactElement | null => iconRegistry[name] ?? null
